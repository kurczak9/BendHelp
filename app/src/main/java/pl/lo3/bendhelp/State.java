package pl.lo3.bendhelp;


/*
 * Basic Data class to hold a state name and the state capital.
 */

public class State {
	
	private float bendRadius;
	private float tubeDiameter;
	private float tubeThickness;
	private String material;


	public float getBendRadius() {
		return bendRadius;
	}

	public void setBendRadius(float bendRadius) {
		this.bendRadius = bendRadius;
	}

	public float getTubeDiameter() {
		return tubeDiameter;
	}

	public void setTubeDiameter(float tubeDiameter) {
		this.tubeDiameter = tubeDiameter;
	}

	public float getTubeThickness() {
		return tubeThickness;
	}

	public void setTubeThickness(float tubeThickness) {
		this.tubeThickness = tubeThickness;
	}

	public String getMaterial() {
		return material;
	}

	public void setMaterial(String material) {
		this.material = material;
	}


	
	

}
